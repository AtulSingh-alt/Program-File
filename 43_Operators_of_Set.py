# 42) Set operators (Union, Intersection, difference & symmetric difference)

A = {1,2,3,4,5,6}
B = {4,5,6,7,8,9}

print(f"The union of the number is : {A | B} ")
print(f"The intersection of the number is : {A & B} ")
print(f"The difference of the number is : {A - B} ")
print(f"The symmetric difference of the number is : {A ^ B} ")