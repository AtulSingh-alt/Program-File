# 1) Calculate the grade with if else

marks = int(input("Enter your score : "))
if marks >= 90:
    print("Your grade is A")
elif marks >= 80:
    print("Your grade is B")
elif marks >= 70:
    print("Your grade is C")
elif marks >= 55:
    print("Your grade is D")
else:
    print("Sorry! You are fail")
